int	ft_ceil_sqrt(unsigned int n)
{
	int sqrt;

	sqrt = 0;
	while (sqrt * sqrt < n)
	{
		if (sqrt > 65535)
			return (-1);
		++sqrt;
	}
	return (sqrt);
}
